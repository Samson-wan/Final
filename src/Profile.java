public class Profile {
    private String cute_name;
    private double purse;

    public Profile(String cute_name, double purse){
        this.cute_name = cute_name;
        this.purse = purse;
    }

    public String getCute_name() {
        return cute_name;
    }

    public double getPurse() {
        return purse;
    }
}
